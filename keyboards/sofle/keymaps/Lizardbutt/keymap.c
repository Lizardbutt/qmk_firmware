bool oled_task_user(void) {
    if(last_input_activity_elapsed() < OLED_SCREENSAVER_TIMEOUT) {
        // Turn the OLED on and get current layer state
        oled_on();
        oled_screensaver_active = false;
        switch (get_highest_layer(layer_state)) {
            case _BASE :
                // Clearing the OLED to omit anything previously
                oled_clear();
                // Writing the PAGE, oled_write() should write below this.
                oled_write_P("----\nMain\nLayer\n----", false);
                break;
            case _NUMPAD : 
                oled_clear();
                oled_write_P("----\nNum\nLayer\n----", false);
                break;
            case _CHARACTERS :
                oled_clear();
                oled_write_P("----\nChar\nLayer\n----", false);
                break;
        }
    }
    // If the oled is on, but has been idle for longer than the screensaver time, turn the OLED off
    if(is_oled_on() && last_input_activity_elapsed() > OLED_TIMEOUT) {
        oled_off();
        oled_screensaver_active = false;
    // If the OLED is on, but has been idle for a while, turn the screensaver on
    } else if(is_oled_on() && last_input_activity_elapsed() > OLED_SCREENSAVER_TIMEOUT) {
        oled_screensaver_active = true;
    }
    // If idle, render the animation.
    if (oled_screensaver_active) {
        // Use some render animation
        render_animation();
        return false;
    }
    return false;
}

